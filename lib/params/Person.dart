import 'package:json_annotation/json_annotation.dart';

class Person {
  int age;
  String college;
  String headpic;
  String major;
  String nickname;
  String school;
  int sex;
  String sign;
  String tags;

  Person(this.age, this.college, this.headpic, this.major, this.nickname,
      this.school, this.sex, this.sign, this.tags);
}